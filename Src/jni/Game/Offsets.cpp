	GameID GetGameID(unsigned int magic_num)
	{
		if (magic_num == 0x506E0406)
			return GameID::PUBGM_GLOBAL;

		if (magic_num == 0xFF67FF70)
			return GameID::PUBGM_CN;

		return GameID::NONE;
	}


